package com.mock.preparation.ducat.Oops.arrayassignment2;

public class Pattern11 {
    public static void main(String[] args) {

        for (int i = 5; i <= 9; i++) {
            for (int j = 5; j >= 1; j--) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
